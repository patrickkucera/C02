char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char dest[255] = "lasdoadsdas";
	char src[255] = "sourci";
	
	printf("Avant test = %s\n", dest);
	ft_strcpy(dest, src);
	printf("Apres test = %s\n", dest);
}